print("-----Operatoren-----")

zahl1 = 10
zahl2 = 4

ergebnis = zahl1 * zahl2
print(zahl1, "mal", zahl2, "=", ergebnis)
ergebnis = zahl1 ** zahl2
print(zahl1, "hoch", zahl2, "=", ergebnis)

ergebnis = zahl1 / zahl2
print(zahl1, "durch", zahl2, "=", ergebnis)
ergebnis = zahl1 // zahl2
print(zahl1, "durch (Integer)", zahl2, "=", ergebnis)
print("--------------------\n")